    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        //Adiciona os dados a database;
        
        try {
            Aluno aluno = new Aluno();

            aluno.setCoordenador(txtCordenador.getText());
            aluno.setCpf(txtCpf.getText().replaceAll("[^a-zA-Z0-9]", ""));
            aluno.setCurso(txtCurso.getText());
            aluno.setData_nasc(Yagami.stringToDate(txtDatanasc.getText()));
            aluno.setEmail(txtEmail.getText());
            aluno.setEst_civ((String) comboSexo.getSelectedItem());
            aluno.setEtnia((String) comboEtnia.getSelectedItem());
            aluno.setFk_endereco(Integer.parseInt(txtEndereco.getText()));
            aluno.setNome(txtNome.getText());
            aluno.setNome_mae(txtMae.getText());
            aluno.setNome_resp(txtResponsavel.getText());
            aluno.setRa(txtRa.getText());
            aluno.setRg(txtRg.getText());
            aluno.setSexo((String) comboSexo.getSelectedItem());
            aluno.setTel_prim(txtTel1.getText());
            aluno.setTel_sec(txtTel2.getText());
            // Caso consiga cadastrar, limpa todos os campos e volta o foco para
            // o primeiro Text Field
            
            
            if (AlunoC.CONTROL.create(aluno)) {
                comboEstCivil.setSelectedIndex(0);
                comboEtnia.setSelectedIndex(0);
                comboSexo.setSelectedIndex(0);
                for (JTextField tf : textFields) {
                    if (!"".equals(tf.getText())) {
                        tf.setText("");
                    }
                }
                txtNome.requestFocus();
            }
            

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

